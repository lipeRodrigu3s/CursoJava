package Aula1;

public class Operacao {
    public static void main(String[] args) {
        
        double x = 54.6;
        double y = 85.9;

        System.out.println(x * y);
        System.out.println(x - y);
        System.out.println(x + y);
        System.out.println(x / y);

        int a = 10;
        int b = 4;

        System.out.println(a % b);

    }
}
